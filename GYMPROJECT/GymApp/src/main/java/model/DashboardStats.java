package model;

public class DashboardStats {
    private int activeMembers;
    private int totalPlans;
    private int totalEquipment;
    
    // Getters and setters
    public int getActiveMembers() { return activeMembers; }
    public void setActiveMembers(int activeMembers) { this.activeMembers = activeMembers; }
    
    public int getTotalPlans() { return totalPlans; }
    public void setTotalPlans(int totalPlans) { this.totalPlans = totalPlans; }
    
    public int getTotalEquipment() { return totalEquipment; }
    public void setTotalEquipment(int totalEquipment) { this.totalEquipment = totalEquipment; }
}